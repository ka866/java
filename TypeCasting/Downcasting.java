package com.src.TypeCasting;

public class Downcasting {
	
	public static void main(String[] args)
	{
		long salary=897654667;
		int newSalary=(int)salary;
		System.out.println(newSalary);
		
		double income=290173733;
		float newIncome=(float)income;
		System.out.println(newIncome);
		
		short noOfPen=12;
		byte totalPen=(byte)noOfPen;
		System.out.println(totalPen);
		
		float weight=29.7f;
		int newWeight=(int)weight;
		System.out.println(newWeight);
	}
	
	

}
